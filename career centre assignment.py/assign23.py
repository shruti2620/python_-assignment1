# differentiate between append() and extend() methods ?

"""
append() method 

append method is used to add single element at the end of the list .

"""
my_list= [1,2,3,4,5]
my_list.append(6)
print(my_list)

"""
extend() method 

extend method is used to add multiple elements in existing list.

"""
my_list=[1,2,3,4,5]
my_list.extend([6,7,8,9])
print(my_list)